                },
                body: JSON.stringify({ id_token: idToken })
            })
                .then(response => response.json())
                .then(data => {
                    if (data.success) {
                        window.location.href = '/dashboard';
                    } else {
                        alert('Ошибка входа!');
                    }
                })
                .catch(error => console.error('Error:', error));
